export function useI18n() {
  const store = useProjectStore();
  const lang = computed<Lang>(() => (store.config.language === "zh" ? "zh" : "en"));

  function t(key: keyof typeof messages | string, params: Record<string, string | number> = {}) {
    const entry = messages[key as keyof typeof messages];
    let value = entry?.[lang.value] ?? entry?.en ?? key;
    for (const [name, replacement] of Object.entries(params)) {
      value = value.split(`{${name}}`).join(String(replacement));
    }
    return value;
  }

  function status(value: string) {
    return t(`status.${value}`);
  }

  return { lang, t, status };
}
